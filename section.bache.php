<section id="section-bache" class="section">
	<div class="section-content">
		<div>
			<h1>¿Qué es un bache?</h1>
			<p>
				Es el deterioro, hoyo o desnivel de una parte de la carpeta asfática de rodamiento generado por
				filtraciones de agua, desgaste vehicular, deficiencias del suelo o pérdida de meterial asfáltico.
			</p>
		</div>
		<div>
			<h1>¿Qué no es un bache?</h1>
			<div>
				<h2>Zanja:</h2>
				<p>
					Excavación larga y estrecha en la carpeta de rodamiento realizada con fines de construcción de la
					red hidráulica (agua potable y drenaje), instalaciones subterráneas (cablevisión, gas natural,
					canalizaciones eléctricas, alumbrado, CFE) entre otros.
				</p>
			</div>
			<div>
				<h2>Hundimiento:</h2>
				<p>
					Desnivel provocado por asentamientos del subsuelo.
				</p>
			</div>
			<div>
				<h2>Socavón:</h2>
				<p>
					Hundimiento producido en la carpeta de rodamiento generalmente ocasionado por una corriente de agua
					subterránea o algún hueco bajo tierra provocando una apertura.
				</p>
			</div>
			<div>
				<h2>Corte:</h2>
				<p>
					Trabajos realizados en la carpeta de rodamiento por empresas telefónicas, CFE, SACMEX, tuberías,
					gas natural o conexiones particulares al drenaje.
				</p>
			</div>
			<div>
				<h2>Acocodrilado:</h2>
				<p>
					Serie de grietas juntas en un área de la carpeta asfáltica, presentado generalmente por el
					deterioro de la carpeta de rodamiento.
				</p>
			</div>
			<div>
				<h2>Rodera:</h2>
				<p>
					Hundimiento horizontal debido al rodamiento vehicular.
				</p>
			</div>
			<div>
				<h2>Vialidades en obra:</h2>
				<p>
					Aquellas vialidades que se encuentran en mantenimiento de la carpeta asfáltica debido a
					re nivelaciones, bacheo o interviniendo un socavón. Se retrogradarán para llevar a cabo
					hasta finalizar los trabajos.
				</p>
			</div>
		</div>
	</div>
</section>